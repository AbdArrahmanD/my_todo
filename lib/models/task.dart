class Task {
  int id;
  int color;
  int isCompleted;
  String title;
  String note;
  String startTime;
  String endTime;
  String date;
  int remind;
  String repeat;
  Task({
    required this.id,
    required this.color,
    required this.isCompleted,
    required this.title,
    required this.note,
    required this.startTime,
    required this.endTime,
    required this.date,
    required this.repeat,
    required this.remind,
  });
}
