class Task {
  late int id;
  late int color;
  late int isCompleted;
  late String title;
  late String note;
  late String startTime;
  late String endTime;
  late String date;
  Task({
    required this.id,
    required this.color,
    required this.isCompleted,
    required this.title,
    required this.note,
    required this.startTime,
    required this.endTime,
    required this.date,
  });
}
